    private void codigoTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoTxtKeyTyped
        // TODO add your handling code here:
                                if (codigoTxt.getText().length() >= 5) {
            evt.consume();
        }
        char c = evt.getKeyChar();
        if (Character.isLetterOrDigit(c) || evt.getKeyChar() == KeyEvent.VK_SPACE || evt.getKeyChar() == KeyEvent.VK_BACK_SPACE) {
            //Codigo
            String cad = ("" + c).toUpperCase();
            c = cad.charAt(0);
            evt.setKeyChar(c);
            
            avisoCod.setText("Se recomiendan 3 letras y 2 numeros");
        }
    }//GEN-LAST:event_codigoTxtKeyTyped

